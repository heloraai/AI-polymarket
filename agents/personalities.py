"""观点交易所 — 统一的 Agent 性格定义

提供两种格式：
- AGENT_PERSONALITIES: list[dict]  — Web 模式（FastAPI 服务器使用）
- ALL_PERSONALITIES:   list[AgentPersonality] — CLI 模式（命令行工具使用）
- JUDGE_PERSONALITY:   dict — 裁判性格（Web 模式）
- BULL/BEAR/FOX/OWL/DEGEN: AgentPersonality — 单独导出（CLI 模式）
"""

from dataclasses import dataclass


@dataclass
class AgentPersonality:
    name: str           # Agent 名字
